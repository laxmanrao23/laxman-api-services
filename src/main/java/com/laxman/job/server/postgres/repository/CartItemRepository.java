package com.laxman.job.server.postgres.repository;

import com.laxman.job.server.postgres.entity.CartItem;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.stereotype.Repository;

import java.util.List;
import java.util.Optional;

@Repository("postgresCartItemRepository")
public interface CartItemRepository extends JpaRepository<CartItem, Long> {

    List<CartItem> findByUsername(String username);

    Optional<CartItem> findByUsernameAndProductId(String username, Long productId);
}
